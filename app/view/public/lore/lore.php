                <header class="section-header">
                    <h2 class="section-title">L'Évolution de Mon Œuvre</h2>
                    <p class="section-subtitle">Les principales séries qui jalonnent mon parcours artistique</p>
                </header>
                
                <div class="series-container">
                    <article class="series-item">
                        <div class="series-image">
                            <img src="/public/uploads/4_Differents ways to bloom_oil painting_2025_50x75cm.jpg" alt="Série Échos Urbains">
                        </div>
                        <div class="series-content">
                            <span class="series-year">2010 - 2012</span>
                            <h3>Échos Urbains</h3>
                            <p>Cette série, qui a marqué le début de ma reconnaissance internationale, explore la relation complexe entre la nature et l'urbanité. À travers des compositions géométriques et des palettes contrastées, j'ai cherché à capturer la tension entre l'ordre artificiel des constructions humaines et le chaos organique de la nature.</p>
                            <p>Les œuvres de cette période sont caractérisées par des lignes architecturales strictes, adoucies par des interventions organiques et des textures rappelant l'érosion naturelle. Cette dualité reflète notre propre condition contemporaine, oscillant entre structure sociale et impulsions naturelles.</p>
                        </div>
                    </article>
                    
                    <article class="series-item">
                        <div class="series-image">
                            <img src="/public/uploads/3_A universe perched in a tank_oil paintins_2024_90x160cm.jpg" alt="Série Horizons Liquides">
                        </div>
                        <div class="series-content">
                            <span class="series-year">2013 - 2015</span>
                            <h3>Horizons Liquides</h3>
                            <p>Suite à mes voyages en Asie, cette série marque une transition vers une approche plus fluide et méditative. Inspirée par les philosophies orientales et les paysages aquatiques, j'ai développé un langage visuel où les frontières se dissolvent entre ciel et eau, réalité et reflet.</p>
                            <p>La technique s'est adaptée à cette nouvelle vision : couches translucides, glacis subtils et gestes plus libres caractérisent ces œuvres. L'eau devient métaphore de la conscience, surface réfléchissante qui nous renvoie à notre propre intériorité tout en nous connectant à l'immensité du monde.</p>
                        </div>
                    </article>
                    
                    <article class="series-item">
                        <div class="series-image">
                            <img src="/public/uploads/11_death.jpg" alt="Série Reflets Intérieurs">
                        </div>
                        <div class="series-content">
                            <span class="series-year">2016 - 2018</span>
                            <h3>Reflets Intérieurs</h3>
                            <p>Cette série, présentée lors de ma première grande exposition personnelle au Musée d'Art Moderne de Bruxelles, marque un tournant introspectif dans mon travail. Elle explore les territoires de la mémoire, des émotions enfouies et de l'inconscient collectif.</p>
                            <p>Techniquement, j'ai introduit des matériaux non-traditionnels comme tissus, papiers et objets trouvés, créant des surfaces stratifiées qui invitent le regard à plonger dans différentes couches de perception. Ces œuvres fonctionnent comme des palimpsestes visuels, où chaque strate raconte une partie d'une histoire plus vaste, à la fois personnelle et universelle.</p>
                        </div>
                    </article>
                    
                    <article class="series-item">
                        <div class="series-image">
                            <img src="/public/uploads/logo.PNG" alt="Série Frontières Numériques">
                        </div>
                        <div class="series-content">
                            <span class="series-year">2020 - Présent</span>
                            <h3>Frontières Numériques</h3>
                            <p>Ma série la plus récente explore l'intersection entre l'art traditionnel et les technologies numériques. En réponse à notre réalité de plus en plus virtuelle, j'interroge les notions d'authenticité, de reproductibilité et de présence dans un monde où l'expérience devient progressivement médiée par les écrans.</p>
                            <p>Ces œuvres combinent techniques picturales traditionnelles et éléments numériques, créant un dialogue entre matérialité et virtualité. À travers cette fusion, j'invite le spectateur à reconsidérer sa relation au réel et à l'image, dans un contexte où ces frontières deviennent de plus en plus poreuses.</p>
                        </div>
                    </article>
