                    <h2 class="section-title">Influences et Inspirations</h2>
                    <p class="section-subtitle">Les sources qui nourrissent ma créativité</p>
                </header>
                
                <div class="influences-grid">
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-mountain"></i>
                        </div>
                        <h3>Nature et Paysages</h3>
                        <p>La contemplation des paysages belges de mon enfance aux panoramas asiatiques de mes voyages a façonné ma sensibilité aux harmonies chromatiques et aux compositions organiques. Je cherche à capturer cette essence naturelle, non pas dans une représentation littérale, mais dans une interprétation émotionnelle.</p>
                    </article>
                    
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-book"></i>
                        </div>
                        <h3>Littérature et Poésie</h3>
                        <p>Les mots ont toujours guidé mes images. Les œuvres de René Char, Fernando Pessoa et Virginia Woolf ont particulièrement influencé ma conception de l'espace pictural comme lieu de rencontre entre le tangible et l'ineffable, entre l'expérience sensorielle et sa transcription émotionnelle.</p>
                    </article>
                    
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-music"></i>
                        </div>
                        <h3>Musique et Rythme</h3>
                        <p>La musique contemporaine et le jazz expérimental nourrissent ma pratique quotidienne. Les structures répétitives de Steve Reich, les textures sonores de Max Richter ou l'improvisation structurée du jazz m'ont appris à penser la composition picturale en termes de rythme, de silence et de résonnance.</p>
                    </article>
                    
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-landmark"></i>
                        </div>
                        <h3>Traditions Picturales</h3>
                        <p>Mon travail s'inscrit dans un dialogue constant avec l'histoire de l'art. Des atmosphères lumineuses de Turner aux espaces méditatifs de Rothko, en passant par les compositions de Diebenkorn, je puise dans ce patrimoine visuel pour développer mon propre langage, à la croisée de la figuration et de l'abstraction.</p>
                    </article>
                    
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-city"></i>
                        </div>
                        <h3>Architecture et Espace</h3>
                        <p>La façon dont l'architecture structure l'espace et dialogue avec son environnement influence profondément ma conception de la composition. Les œuvres de Tadao Ando, avec leur jeu subtil entre simplicité formelle et richesse expérientielle, m'inspirent particulièrement.</p>
                    </article>
                    
                    <article class="influence-item">
                        <div class="influence-icon">
                            <i class="fas fa-microscope"></i>
                        </div>
                        <h3>Sciences et Perception</h3>
                        <p>Les théories scientifiques sur la perception visuelle, la physique quantique et les neurosciences m'offrent de nouvelles perspectives sur la façon dont nous construisons notre réalité. Ces connaissances nourrissent ma réflexion sur le rôle de l'art comme médiateur entre le visible et l'invisible.</p>
                    </article>
