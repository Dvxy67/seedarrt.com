                    <h2 class="section-title">Le Fil Conducteur</h2>
                    <p class="section-subtitle">Les thèmes récurrents qui traversent l'ensemble de mon œuvre</p>
                </header>
                
                <article class="intro-content">
                    <p>À travers la diversité de mes explorations artistiques, plusieurs fils conducteurs persistent et tissent une trame cohérente entre toutes mes créations. Ces thèmes fondamentaux reflètent mes préoccupations philosophiques et esthétiques les plus profondes.</p>
                    
                    <p>L'interaction entre lumière et ombre reste au cœur de ma pratique, symbolisant la dualité inhérente à l'expérience humaine. Je m'intéresse particulièrement aux moments de transition – crépuscule, aube, changement de saison – ces instants où une réalité cède la place à une autre, créant un espace liminal riche en possibilités.</p>
                    
                    <p>La question de la mémoire et de sa fragilité imprègne également mon travail. Comment les souvenirs se transforment-ils avec le temps ? Comment l'art peut-il préserver l'éphémère tout en acceptant sa nature transitoire ? Ces interrogations me poussent à explorer les limites entre présence et absence, entre ce qui persiste et ce qui s'efface.</p>
                    
                    <blockquote class="intro-quote">Mon œuvre est une invitation au voyage intérieur, un espace où le visible et l'invisible se rencontrent pour former une nouvelle réalité.</blockquote>
                    
                    <p>Enfin, la tension entre ordre et chaos structure ma démarche esthétique. Dans un monde de plus en plus fragmenté, je cherche à créer des œuvres qui reconnaissent cette complexité tout en proposant des moments d'harmonie et d'équilibre. Cette recherche se manifeste tant dans mes compositions que dans le traitement de la matière, oscillant entre contrôle et lâcher-prise.</p>
                    
                    <p>Ces thèmes ne sont pas abordés de manière didactique, mais plutôt comme des courants souterrains qui irriguent l'ensemble de ma production, créant des résonances et des échos entre des œuvres parfois distantes dans le temps et dans la forme.</p>
