                    <h2 class="section-title">Ma Démarche Artistique</h2>
                    <p class="section-subtitle">Un voyage à travers les émotions, les textures et les histoires</p>
                    
                    <p>Ma démarche artistique est un dialogue constant entre mon vécu intérieur et l'observation du monde qui m'entoure. Chaque œuvre est une exploration, un moment suspendu qui capture à la fois l'éphémère et l'éternel. Je cherche à révéler ce qui se cache derrière la surface des choses, à saisir l'essence des émotions humaines et la beauté cachée dans l'ordinaire.</p>
                    
                    <blockquote class="intro-quote">L'art est ma façon de questionner le visible et d'explorer l'invisible, de rendre tangible ce qui ne l'est pas encore.</blockquote>
                    
                    <p>À travers les années, mon travail s'est développé en plusieurs séries distinctes mais interconnectées. Ces collections forment un écosystème artistique où chaque œuvre dialogue avec les autres, créant un réseau de sens et d'émotions. De mes premières explorations chromatiques aux œuvres plus récentes, un fil conducteur persiste : la recherche d'une authenticité visuelle et émotionnelle.</p>
                </article>
